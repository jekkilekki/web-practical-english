    <ol><h3>What's in this book</h3>
      <li><h6>Section 1: Parts of Speech</h6>
          <p>To help students get a full understanding of proper sentence creation, the book starts with the basics: an introduction to nouns, pronouns, verbs, adjectives, and adverbs.</p>
      </li>
      <li><h6>Section 2: Sentence Formation</h6>
          <p>Once students have a solid grasp on the Parts of Speech, the book ventures on into solid sentence formation. This part of the book covers basic sentences structure, prepositions of place, and writing descriptive sentences.</p>
      </li>
      <li><h6>Section 3: Forms &amp; Letters</h6>
          <p>Practical application time! Using various forms and written documents, students are led through the process of putting word to page in multiple real-world examples. These include address writing, travel forms, postcards, letters, and travel brochures.</p>
      </li>
      <li><h6>Section 4: References &amp; Notes</h6>
          <p>In Section 4, teachers can find 3 Sample Tests that include various types of questions that weâ€™ve found effective in our own tests. And, at the very back of the book, students can find a 10-page lined section for taking notes throughout the class.</p>
      </li>
    </ol>
