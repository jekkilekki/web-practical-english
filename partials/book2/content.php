    <ol><h3>What's in this book</h3>
      <li><h6>Section 1: Review</h6>
          <p>Section One reviews the Parts of Speech learned in Practical English Writing Book One. Students also review prepositions and go into greater detail with them.</p>
      </li>
      <li><h6>Section 2: Punctuation</h6>
          <p>Section Two covers the basics of English punctuation including periods, exclamation marks, question marks, commas, and apostrophes.</p>
      </li>
      <li><h6>Section 3: Sentence Types</h6>
          <p>Section Three goes over three basic types of sentences and how to create them. Students will learn Simple, Compound, and Complex Sentences as well as Independent and Dependent Clauses, Predicates, Conjunctions, and Subordinators.</p>
      </li>
      <li><h6>Section 4: Paragraphs</h6>
          <p>Section Four covers basic paragraph formation and provides students ample opportunity to practice writing with simple, narrative, and persuasive paragraphs.</p>
      </li>
      <li><h6>Section 5: Resources</h6>
          <p>Section Five includes two Practice Exams using real questions weâ€™ve used in our own classes. There are also two additional Outline, Rough Draft, Revising, and Final Draft sections for students who want more practice writing in the format presented in this book.</p>
      </li>
    </ol>
