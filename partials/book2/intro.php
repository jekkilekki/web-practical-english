    <h2>Practice English Writing II</h2>
    <p>This book is intended to meet the needs of low-level, young adult learners. For many years, the authors of the Practical English Writing series have noted an inconvenient gap in available ESL textbooks. The books that offer level-appropriate materials for our university students have childish images and themes, while those suited to our students’ ages are too advanced or move too quickly. Unable to discover any textbook that combines adult appeal with basic English writing, we’ve taken the plunge and designed our own.</p>
    <p>The Practical English Writing series uses current ESL best practices and theories for the best learner outcomes. This material has been taught and tested over the course of six semesters, and received high ratings from students every time. We hope it proves as useful and entertaining in your classroom as it does in ours.</p>
